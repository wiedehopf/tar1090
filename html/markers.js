/**
 * Get the scaled shape for a given icon label
 * @param {Marker} marker
 * @param {number} globalScale 
 */
function getShapeWithSize(marker, globalScale = 1) {
  const label = marker[1];
  const entry = getEntry(marker);

  let scaling = 1;
  if (isScaledTypeDesignation(entry)) {
    scaling = entry[1];
  }
  let shape = shapes[entry[0]];

  let { w, h } = scaleShape(label, entry, shape)
  w = Math.round(w * scaling * globalScale * 1000) / 1000;
  h = Math.round(h * scaling * globalScale * 1000) / 1000;

  // Ensure shape fits within icon size, needed f.e. when scaling by area
  if (w > glIconSize) {
    log(label, 'Width exceeds icon size, scaling down');
    w = glIconSize;
    h = (glIconSize / w) * h;
  }
  if (h > glIconSize) {
    log(label, 'Height exceeds icon size, scaling down');
    h = glIconSize;
    w = (glIconSize / h) * w;
  }
  log(label, 'Scaled shape to global iconSize -> width:', w, 'height:', h);

  return {
    ...shape,
    w,
    h,
  }
}

